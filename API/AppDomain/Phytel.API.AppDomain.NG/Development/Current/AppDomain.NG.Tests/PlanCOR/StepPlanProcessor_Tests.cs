        [TestMethod()]
        public void SetCompletedStepResponses_Not_Single_Select_Test()
        {
            StepPlanProcessor spp = new StepPlanProcessor();
            PlanElementEventArg pe = new PlanElementEventArg() {  };
            Step s = new Step{StepTypeId = 12};
            s.SelectedResponseId = "123456789012345678901234";
            s.Responses = new List<Response>();
            s.Responses.Add(new Response{ Id="123456789012345678901234", Value ="6"});
            spp.SetCompletedStepResponses(pe, s);
            Assert.Fail();
        }

